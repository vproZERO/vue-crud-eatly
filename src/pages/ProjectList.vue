<template>
  <layout-div>
    <div class="container">
      <h2 class="text-center mt-5 mb-3">Project Manager</h2>
      <div class="card">
        <div class="card-header">
          <router-link to="/admin/create" class="btn btn-outline-primary"
            >Create New Project</router-link
          >
        </div>
        <div class="card-body overflow-auto">
          <table class="table table-bordered ">
            <thead>
              <tr>
                <th>Name</th>
                <th>Img</th>
                <th>Description</th>
                <th width="240px">Action</th>
              </tr>
            </thead>
            <tbody>
              <tr v-for="project in projects" :key="project">
                <td>{{ project.name }}</td>
                <td class="">{{project.img}}</td>
                <td>{{ project.description }}</td>
                <td>
                  <button
                    @click="handleDelete(project.id)"
                    class="btn btn-outline-danger"
                  >
                    Delete
                  </button>
                </td>
              </tr>
            </tbody>
          </table>
        </div>
      </div>
    </div>
  </layout-div>
</template>

<script>
import axios from "axios";
import Swal from "sweetalert2";
import LayoutDiv from "../LayoutDiv.vue";
export default {
    name: 'ProjectList',
    components: {
    LayoutDiv,
  },
  data() {
    return {
        posts: [],
        projects: []
    }
  },
  created() {
    this.fetchProjectList();
  },
  methods: {
    fetchProjectList() {
        axios.get('http://localhost:5000/products')
        .then(response => {
            this.projects = response.data;
            return response
        })
        .catch(error => {
            return error
        })
    },
    handleDelete(id) {
        Swal.fire({
            title:  'Are you sure?',
            text: "You don't be able to revert this!",
            icon: 'warning',
            showCancelButton: true,
            confirmButtonColor: '#3085d6',
            cancelButtonColor: '#d33',
            confirmButtonText: 'Yes, delete it!'
        }).then((result) => {
            if(result.isConfirmed) {
                axios.delete(`http://localhost:5000/products/${id}`)
                .then(response => {
                    Swal.fire({
                        icon: 'success',
                        title: 'Project deleted successfully!',
                        showConfirmButton: false,
                        timer: 1500
                    })
                    this.fetchProjectList();
                    return response
                })
                .catch(error => {
                    Swal.fire({
                        icon: 'error',
                        title: 'An Error Occured!',
                        showConfirmButton: false,
                        timer: 1500
                    })
                    return error
                })
            }
        })
    }
  },
};
</script>

